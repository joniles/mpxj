   /**
    * Apply a single field value to a calendar.
    *
    * @param calendar target calendar
    * @param fieldName field name (in lowercase as written by JsonWriter)
    * @param value field value
    */
   private static void applyFieldToCalendar(ProjectCalendar calendar, String fieldName, Object value)
   {
      if (value == null)
      {
         return;
      }

      switch (fieldName)
      {
         case "unique_id":
            Integer uniqueIdValue = safeIntValue(value);
            if (uniqueIdValue != null)
            {
               calendar.setUniqueID(uniqueIdValue);
            }
            break;

         case "guid":
            if (value instanceof String)
            {
               calendar.setGUID(UUID.fromString((String) value));
            }
            break;

         case "parent_unique_id":
            if (value instanceof Number)
            {
               // Note: ProjectCalendar doesn't have setParentUniqueID method
               // This would need to be handled differently in a real implementation
            }
            break;

         case "name":
            if (value instanceof String)
            {
               calendar.setName((String) value);
            }
            break;

         case "type":
            if (value instanceof String)
            {
               calendar.setType(CalendarType.valueOf(((String) value).toUpperCase()));
            }
            break;

         case "personal":
            if (value instanceof Boolean)
            {
               calendar.setPersonal(((Boolean) value).booleanValue());
            }
            break;

         case "minutes_per_day":
            Integer minutesPerDayValue = safeIntValue(value);
            if (minutesPerDayValue != null)
            {
               calendar.setCalendarMinutesPerDay(minutesPerDayValue);
            }
            break;

         case "minutes_per_week":
            Integer minutesPerWeekValue = safeIntValue(value);
            if (minutesPerWeekValue != null)
            {
               calendar.setCalendarMinutesPerWeek(minutesPerWeekValue);
            }
            break;

         case "minutes_per_month":
            Integer minutesPerMonthValue = safeIntValue(value);
            if (minutesPerMonthValue != null)
            {
               calendar.setCalendarMinutesPerMonth(minutesPerMonthValue);
            }
            break;

         case "minutes_per_year":
            Integer minutesPerYearValue = safeIntValue(value);
            if (minutesPerYearValue != null)
            {
               calendar.setCalendarMinutesPerYear(minutesPerYearValue);
            }
            break;

         // Handle exceptions array
         case "exceptions":
            parseCalendarExceptions(calendar, value);
            break;

         // Handle days object (when days are nested under "days" field)
         case "days":
            parseDaysObject(calendar, value);
            break;

         // Note: working_weeks would require more complex parsing
         // For now, we'll handle the basic calendar properties
         default:
            // Handle day definitions (monday, tuesday, etc.) when they're direct fields
            if (isDayOfWeek(fieldName))
            {
               parseDayDefinition(calendar, fieldName, value);
            }
            break;
      }
   }

   /**
    * Check if a field name represents a day of the week.
    */
   private static boolean isDayOfWeek(String fieldName)
   {
      String[] days = {"monday", "tuesday", "wednesday", "thursday", "friday", "saturday", "sunday"};
      String lowerFieldName = fieldName.toLowerCase();
      for (String day : days)
      {
         if (lowerFieldName.equals(day))
         {
            return true;
         }
      }
      return false;
   }

   /**
    * Parse the days object that contains day definitions.
    */
   private static void parseDaysObject(ProjectCalendar calendar, Object value)
   {
      if (!(value instanceof Map))
      {
         return;
      }

      @SuppressWarnings("unchecked")
      Map<String, Object> daysMap = (Map<String, Object>) value;
      
      for (Map.Entry<String, Object> entry : daysMap.entrySet())
      {
         String dayName = entry.getKey();
         Object dayValue = entry.getValue();
         
         if (isDayOfWeek(dayName))
         {
            parseDayDefinition(calendar, dayName, dayValue);
         }
      }
   }

   /**
    * Parse a day definition from JSON.
    */
   private static void parseDayDefinition(ProjectCalendar calendar, String dayName, Object value)
   {
      if (!(value instanceof Map))
      {
         return;
      }

      @SuppressWarnings("unchecked")
      Map<String, Object> dayData = (Map<String, Object>) value;
      
      // Get the day of week
      DayOfWeek dayOfWeek = DayOfWeek.valueOf(dayName.toUpperCase());
      
      // Parse day type (handle both lowercase and uppercase)
      String type = (String) dayData.get("type");
      if (type != null)
      {
         // Convert to uppercase for MPXJ enum
         String upperType = type.toUpperCase();
         DayType dayType = DayType.valueOf(upperType);
         calendar.setCalendarDayType(dayOfWeek, dayType);
      }
      
      // Parse working hours if present
      @SuppressWarnings("unchecked")
      List<Map<String, Object>> hours = (List<Map<String, Object>>) dayData.get("hours");
      if (hours != null && !hours.isEmpty())
      {
         ProjectCalendarHours calendarHours = calendar.getCalendarHours(dayOfWeek);
         if (calendarHours == null)
         {
            calendarHours = calendar.addCalendarHours(dayOfWeek);
         }
         
         for (Map<String, Object> hourRange : hours)
         {
            String fromTime = (String) hourRange.get("from");
            String toTime = (String) hourRange.get("to");
            if (fromTime != null && toTime != null)
            {
               // Handle time format with or without seconds
               LocalTime start = parseTime(fromTime);
               LocalTime end = parseTime(toTime);
               if (start != null && end != null)
               {
                  calendarHours.add(new LocalTimeRange(start, end));
               }
            }
         }
      }
   }

   /**
    * Parse a time string that may or may not include seconds.
    *
    * @param timeString time string (e.g., "08:00" or "08:00:00")
    * @return LocalTime or null if parsing fails
    */
   private static LocalTime parseTime(String timeString)
   {
      if (timeString == null || timeString.trim().isEmpty())
      {
         return null;
      }

      try
      {
         // If no seconds provided, add :00
         if (timeString.matches("\\d{1,2}:\\d{2}$"))
         {
            timeString = timeString + ":00";
         }
         
         return LocalTime.parse(timeString);
      }
      catch (Exception e)
      {
         System.err.println("Warning: Could not parse time: " + timeString);
         return null;
      }
   }

   /**
    * Parse calendar exceptions from JSON.
    *
    * @param calendar target calendar
    * @param value exceptions array value
    */
   private static void parseCalendarExceptions(ProjectCalendar calendar, Object value)
   {
      if (!(value instanceof List))
      {
         return;
      }

      @SuppressWarnings("unchecked")
      List<Map<String, Object>> exceptions = (List<Map<String, Object>>) value;
      
      for (Map<String, Object> exceptionData : exceptions)
      {
         try
         {
            String name = (String) exceptionData.get("name");
            String fromDateStr = (String) exceptionData.get("from");
            String toDateStr = (String) exceptionData.get("to");
               // String typeStr = (String) exceptionData.get("type"); // Not used since getWorking() is read-only
            
            if (fromDateStr != null && toDateStr != null)
            {
               LocalDate fromDate = LocalDate.parse(fromDateStr);
               LocalDate toDate = LocalDate.parse(toDateStr);
               
               // Create calendar exception
               ProjectCalendarException exception = calendar.addCalendarException(fromDate, toDate);
               exception.setName(name);
               
               // For non-working exceptions, we don't add any working hours
               // For working exceptions, we could add default working hours if needed
               // The getWorking() method will return true if there are working hours, false otherwise
            }
         }
         catch (Exception e)
         {
            System.err.println("Warning: Could not parse calendar exception: " + e.getMessage());
         }
      }
   }

   /**
    * Apply field values to a resource object.
    *
    * @param resource target resource
    * @param fieldValues map of field names to values
    */
   private static void applyFieldValuesToResource(Resource resource, Map<String, Object> fieldValues)
   {
      for (Map.Entry<String, Object> entry : fieldValues.entrySet())
      {
         String fieldName = entry.getKey();
         Object value = entry.getValue();
         
         try
         {
            ResourceField field = getResourceFieldFromName(fieldName);
            if (field != null)
            {
               Object convertedValue = convertValueForResourceField(value, field);
               if (convertedValue != null)
               {
                  resource.set(field, convertedValue);
               }
            }
         }
         catch (Exception e)
         {
            System.err.println("Warning: Could not apply resource field " + fieldName + ": " + e.getMessage());
         }
      }
   }

   /**
    * Get ResourceField enum from lowercase field name.
    */
   private static ResourceField getResourceFieldFromName(String fieldName)
   {
      String enumName = fieldName.toUpperCase();
      try
      {
         return ResourceField.valueOf(enumName);
      }
      catch (IllegalArgumentException e)
      {
         return null;
      }
   }

   /**
    * Convert a value to the appropriate type for a resource field.
    */
   private static Object convertValueForResourceField(Object value, ResourceField field)
   {
      // Similar logic to convertValueForField but for resource fields
      if (value == null)
      {
         return null;
      }

      switch (field.getDataType())
      {
         case SHORT:
         case INTEGER:
            Integer intValue = safeIntValue(value);
            if (intValue != null)
            {
               return intValue;
            }
            break;

         case NUMERIC:
         case PERCENTAGE:
         case CURRENCY:
         case UNITS:
            Double doubleValue = safeDoubleValue(value);
            if (doubleValue != null)
            {
               return doubleValue;
            }
            break;

         case BOOLEAN:
            if (value instanceof Boolean)
            {
               return value;
            }
            break;

         case STRING:
            return value.toString();

         case RESOURCE_TYPE:
            if (value instanceof String)
            {
               return ResourceType.valueOf((String) value);
            }
            break;

         default:
            return value;
      }

      return value;
   }

   /**
    * Set up a project from JSON containing property_values, custom_fields, and resources.
    */
   public static ProjectFile setupProjectFromJson(String projectJson) throws IOException
   {
      ProjectFile project = new ProjectFile();
      
      com.fasterxml.jackson.core.JsonFactory factory = new com.fasterxml.jackson.core.JsonFactory();
      com.fasterxml.jackson.core.JsonParser parser = factory.createParser(projectJson);
      
      ProjectProperties props = project.getProjectProperties();
      CustomFieldContainer customFields = project.getCustomFields();
      
      com.fasterxml.jackson.core.JsonToken token = parser.nextToken();
      if (token != com.fasterxml.jackson.core.JsonToken.START_OBJECT)
      {
         throw new IOException("Expected JSON object start");
      }
      
      while (parser.nextToken() != com.fasterxml.jackson.core.JsonToken.END_OBJECT)
      {
         String fieldName = parser.currentName();
         parser.nextToken();
         
         switch (fieldName)
         {
            case "property_values":
               parsePropertyValues(parser, props);
               break;
            case "custom_fields":
               parseCustomFields(parser, customFields);
               break;
            case "resources":
               parseResources(parser, project);
               break;
            default:
               parser.skipChildren();
               break;
         }
      }
      
      parser.close();
      return project;
   }

