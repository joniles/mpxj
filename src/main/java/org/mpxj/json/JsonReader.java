   private static void parseCustomFields(com.fasterxml.jackson.core.JsonParser parser, CustomFieldContainer customFields) throws IOException
   {
      if (parser.getCurrentToken() != com.fasterxml.jackson.core.JsonToken.START_ARRAY)
      {
         return;
      }
      
      while (parser.nextToken() != com.fasterxml.jackson.core.JsonToken.END_ARRAY)
      {
         if (parser.getCurrentToken() != com.fasterxml.jackson.core.JsonToken.START_OBJECT)
         {
            continue;
         }
         
         String fieldType = null;
         String fieldAlias = null;
         String fieldTypeClass = null;
         
         while (parser.nextToken() != com.fasterxml.jackson.core.JsonToken.END_OBJECT)
         {
            String fieldName = parser.currentName();
            parser.nextToken();
            
            switch (fieldName)
            {
               case "field_type":
                  fieldType = parser.getText();
                  break;
               case "field_alias":
                  fieldAlias = parser.getText();
                  break;
               case "field_type_class":
                  fieldTypeClass = parser.getText();
                  break;
               default:
                  break;
            }
         }
         
         // Create custom field if we have the required info
         if ("task".equals(fieldTypeClass) && fieldType != null && fieldAlias != null)
         {
            org.mpxj.TaskField taskField = parseTaskField(fieldType);
            if (taskField != null)
            {
               CustomField customField = customFields.add(taskField);
               customField.setAlias(fieldAlias);
            }
         }
      }
   }

   private static void parseResources(com.fasterxml.jackson.core.JsonParser parser, ProjectFile project) throws IOException
   {
      if (parser.getCurrentToken() != com.fasterxml.jackson.core.JsonToken.START_ARRAY)
      {
         return;
      }
      
      while (parser.nextToken() != com.fasterxml.jackson.core.JsonToken.END_ARRAY)
      {
         if (parser.getCurrentToken() != com.fasterxml.jackson.core.JsonToken.START_OBJECT)
         {
            continue;
         }
         
         Resource resource = project.addResource();
         
         while (parser.nextToken() != com.fasterxml.jackson.core.JsonToken.END_OBJECT)
         {
            String fieldName = parser.currentName();
            parser.nextToken();
            
            switch (fieldName)
            {
               case "name":
                  resource.setName(parser.getText());
                  break;
               case "type":
                  resource.setType(parseResourceType(parser.getText()));
                  break;
               case "active":
                  resource.setActive(parser.getBooleanValue());
                  break;
               case "max_units":
                  // Note: MaxUnits is typically calculated, not set directly
                  break;
               default:
                  parser.skipChildren();
                  break;
            }
         }
      }
   }

