   /**
    * Read assignments from a JSON string and add them to the project.
    *
    * @param project target project
    * @param assignmentsJson JSON string containing assignments array
    * @throws IOException if JSON parsing fails
    */
   public static void readAssignments(ProjectFile project, String assignmentsJson) throws IOException
   {
      JsonFactory factory = new JsonFactory();
      try (JsonParser parser = factory.createParser(assignmentsJson))
      {
         parser.nextToken(); // Move to START_OBJECT
         
         while (parser.nextToken() != JsonToken.END_OBJECT)
         {
            String fieldName = parser.currentName();
            if (fieldName == null) continue;
            
            parser.nextToken();
            
            if ("assignments".equals(fieldName) && parser.getCurrentToken() == JsonToken.START_ARRAY)
            {
               while (parser.nextToken() != JsonToken.END_ARRAY)
               {
                  readAssignment(project, parser);
               }
            }
            else
            {
               parser.skipChildren();
            }
         }
      }
   }

   /**
    * Read a single assignment from JSON parser.
    */
   private static void readAssignment(ProjectFile project, JsonParser parser) throws IOException
   {
      // We need to collect the assignment data first, then create the assignment
      Map<String, Object> assignmentData = new HashMap<>();
      
      while (parser.nextToken() != JsonToken.END_OBJECT)
      {
         String fieldName = parser.currentName();
         if (fieldName == null) continue;
         
         parser.nextToken();
         Object value = parseFieldValue(parser);
         assignmentData.put(fieldName, value);
      }
      
      // Now create the assignment with task and resource
      Integer taskId = safeIntValue(assignmentData.get("task_unique_id"));
      Integer resourceId = safeIntValue(assignmentData.get("resource_unique_id"));
      
      if (taskId != null && resourceId != null)
      {
         Task task = findTaskByUniqueId(project, taskId);
         Resource resource = findResourceByUniqueId(project, resourceId);
         
         if (task != null && resource != null)
         {
            ResourceAssignment assignment = task.addResourceAssignment(resource);
            
            // Apply all the field values
            for (Map.Entry<String, Object> entry : assignmentData.entrySet())
            {
               applyFieldToAssignment(assignment, entry.getKey(), entry.getValue());
            }
         }
         else
         {
            System.err.println("Warning: Could not find task (ID: " + taskId + ") or resource (ID: " + resourceId + ") for assignment");
         }
      }
      else
      {
         System.err.println("Warning: Assignment missing task_unique_id or resource_unique_id");
      }
   }

   /**
    * Apply a field value to a resource assignment.
    */
   private static void applyFieldToAssignment(ResourceAssignment assignment, String fieldName, Object value)
   {
      if (value == null)
      {
         return;
      }

      // Handle special fields that need special processing
      switch (fieldName.toLowerCase())
      {
         case "task_unique_id":
         case "resource_unique_id":
            // These are handled during assignment creation, skip them here
            return;
            
         case "assignment_units":
            Double units = safeDoubleValue(value);
            if (units != null)
            {
               assignment.setUnits(units);
            }
            return;
            
         case "remaining_assignment_units":
            Double remainingUnits = safeDoubleValue(value);
            if (remainingUnits != null)
            {
               assignment.setRemainingUnits(remainingUnits);
            }
            return;
            
         case "work":
            if (value instanceof Duration)
            {
               assignment.setWork((Duration) value);
            }
            return;
            
         case "remaining_work":
            if (value instanceof Duration)
            {
               assignment.setRemainingWork((Duration) value);
            }
            return;
            
         case "regular_work":
            if (value instanceof Duration)
            {
               assignment.setRegularWork((Duration) value);
            }
            return;
            
         case "actual_work":
            if (value instanceof Duration)
            {
               assignment.setActualWork((Duration) value);
            }
            return;
            
         case "start":
            if (value instanceof String)
            {
               LocalDateTime startDate = parseDateTime((String) value);
               if (startDate != null)
               {
                  assignment.setStart(startDate);
               }
            }
            return;
            
         case "finish":
            if (value instanceof String)
            {
               LocalDateTime finishDate = parseDateTime((String) value);
               if (finishDate != null)
               {
                  assignment.setFinish(finishDate);
               }
            }
            return;
            
         case "resume":
            if (value instanceof String)
            {
               LocalDateTime resumeDate = parseDateTime((String) value);
               if (resumeDate != null)
               {
                  assignment.setResume(resumeDate);
               }
            }
            return;
            
         case "stop":
            if (value instanceof String)
            {
               LocalDateTime stopDate = parseDateTime((String) value);
               if (stopDate != null)
               {
                  assignment.setStop(stopDate);
               }
            }
            return;
            
         case "guid":
            if (value instanceof String)
            {
               try
               {
                  assignment.setGUID(UUID.fromString((String) value));
               }
               catch (Exception e)
               {
                  System.err.println("Warning: Invalid GUID format: " + value);
               }
            }
            return;
            
         case "created":
            // Created date is typically read-only, skip it
            return;
            
         case "rate_source":
            if (value instanceof String)
            {
               try
               {
                  org.mpxj.RateSource rateSource = org.mpxj.RateSource.valueOf(((String) value).toUpperCase());
                  assignment.setRateSource(rateSource);
               }
               catch (Exception e)
               {
                  System.err.println("Warning: Invalid rate source: " + value);
               }
            }
            return;
            
         case "calculate_costs_from_units":
            if (value instanceof Boolean)
            {
               assignment.setCalculateCostsFromUnits((Boolean) value);
            }
            return;
            
         default:
            // Try to map to AssignmentField enum
            AssignmentField field = getAssignmentFieldFromName(fieldName);
            if (field != null)
            {
               Object convertedValue = convertValueForAssignmentField(value, field);
               if (convertedValue != null)
               {
                  assignment.set(field, convertedValue);
               }
            }
            break;
      }
   }

