   /**
    * Parse task relations (predecessors or successors) from JSON.
    *
    * @param task target task
    * @param value relation array from JSON
    * @param isPredecessors true for predecessors, false for successors
    */
   private static void parseTaskRelations(Task task, Object value, boolean isPredecessors)
   {
      if (!(value instanceof List))
      {
         System.err.println("Warning: Expected array for " + (isPredecessors ? "predecessors" : "successors"));
         return;
      }

      @SuppressWarnings("unchecked")
      List<Map<String, Object>> relations = (List<Map<String, Object>>) value;
      
      for (Map<String, Object> relationData : relations)
      {
         try
         {
            // Get the related task unique ID
            Integer relatedTaskId = getIntegerFromMap(relationData, "unique_id");
            if (relatedTaskId == null)
            {
               System.err.println("Warning: Missing unique_id in relation");
               continue;
            }

            // Find the related task in the project
            Task relatedTask = findTaskByUniqueId(task.getParentFile(), relatedTaskId);
            if (relatedTask == null)
            {
               System.err.println("Warning: Could not find task with unique_id: " + relatedTaskId);
               continue;
            }

               // Get lag duration (default to 0)
               Duration lag = Duration.getInstance(0, TimeUnit.DAYS);
               Object lagData = relationData.get("lag");
               if (lagData instanceof Duration)
               {
                  // lag is already a Duration object
                  lag = (Duration) lagData;
               }
               else if (lagData instanceof Map)
               {
                  // lag is a map with duration and units
                  @SuppressWarnings("unchecked")
                  Map<String, Object> lagMap = (Map<String, Object>) lagData;
                  Double lagDurationWrapper = getDoubleFromMap(lagMap, "duration");
                  String lagUnits = getStringFromMap(lagMap, "units");
                  if (lagDurationWrapper != null && lagUnits != null)
                  {
                     try
                     {
                        TimeUnit units = TimeUnit.valueOf(lagUnits.toUpperCase());
                        lag = Duration.getInstance(lagDurationWrapper.doubleValue(), units);
                     }
                     catch (Exception e)
                     {
                        System.err.println("Warning: Invalid lag units: " + lagUnits);
                     }
                  }
               }

            // Get relation type (default to FINISH_START)
            String typeStr = getStringFromMap(relationData, "type");
            RelationType type = RelationType.FINISH_START; // Default
            if (typeStr != null)
            {
               try
               {
                  // Convert short form to full form
                  String fullTypeStr = typeStr.toUpperCase();
                  switch (fullTypeStr)
                  {
                     case "FS": fullTypeStr = "FINISH_START"; break;
                     case "SS": fullTypeStr = "START_START"; break;
                     case "FF": fullTypeStr = "FINISH_FINISH"; break;
                     case "SF": fullTypeStr = "START_FINISH"; break;
                  }
                  type = RelationType.valueOf(fullTypeStr);
               }
               catch (Exception e)
               {
                  System.err.println("Warning: Invalid relation type: " + typeStr + ", using default");
               }
            }

            // Create the relation using the builder pattern
            if (isPredecessors)
            {
               // For predecessors: relatedTask is predecessor, task is successor
               Relation.Builder builder = new Relation.Builder()
                  .predecessorTask(relatedTask)
                  .successorTask(task)
                  .type(type)
                  .lag(lag);
               
               // Set notes if provided
               String notes = getStringFromMap(relationData, "notes");
               if (notes != null)
               {
                  builder.notes(notes);
               }
               
               task.addPredecessor(builder);
            }
            else
            {
               // For successors: task is predecessor, relatedTask is successor
               Relation.Builder builder = new Relation.Builder()
                  .predecessorTask(task)
                  .successorTask(relatedTask)
                  .type(type)
                  .lag(lag);
               
               // Set notes if provided
               String notes = getStringFromMap(relationData, "notes");
               if (notes != null)
               {
                  builder.notes(notes);
               }
               
               relatedTask.addPredecessor(builder);
            }
         }
         catch (Exception e)
         {
            System.err.println("Warning: Could not parse relation: " + e.getMessage());
         }
      }
   }

